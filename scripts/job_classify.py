_SECONDARY_DEFAULT = "制造业"


def _match_industry_secondary(title: str) -> str:
    for kws, ind in _SECONDARY_INDUSTRY:
        if any(k in title for k in kws):
            return ind
    return _SECONDARY_DEFAULT


def classify_job(title: str) -> tuple[str, str]:
    """
    返回 (sector, industryLabel)。
    sector ∈ {primary, secondary, tertiary}
    """
    t = title.strip()
    for keywords, sector, industry in _RULES:
        if any(kw in t for kw in keywords):
            return sector, industry

    # 兜底：常见工种后缀
    if any(k in t for k in ("操作工", "普工", "技工", "工（", "工人", "机长", "巡检员", "运行工")):
        return "secondary", _match_industry_secondary(t)

    if any(k in t for k in ("员", "师", "专员", "顾问", "助理", "主持", "店长")):
        return "tertiary", _TERTIARY_DEFAULT

    return "tertiary", _TERTIARY_DEFAULT

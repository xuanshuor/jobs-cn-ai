_MERGE_PREFIX = frozenset({
    "KTV", "4S", "110", "BIM", "ETC", "AGV", "MES", "OA", "RPA", "TMS", "WMS",
    "B2B", "RPO", "SEO", "SEM", "EAP", "ICU", "CRA", "CRC", "DTP", "DRG", "GIS",
    "AIGC", "HR", "IT", "3C", "MSL", "RLHF", "NLP", "IoT", "ESG", "FP&A", "Copilot",
})


def _tokenize_title_line(line: str) -> list[str]:
    """按空格分词，但括号内空格不拆（如「数据标注（3D 点云）」）。"""
    tokens: list[str] = []
    buf: list[str] = []
    depth = 0
    for ch in line.strip():
        if ch in "（(":
            depth += 1
            buf.append(ch)
        elif ch in "）)":
            depth = max(0, depth - 1)
            buf.append(ch)
        elif ch == " " and depth == 0:
            if buf:
                tokens.append("".join(buf))
                buf = []
        else:
            buf.append(ch)
    if buf:
        tokens.append("".join(buf))
    return _merge_title_tokens(tokens)


def _merge_title_tokens(tokens: list[str]) -> list[str]:
    out: list[str] = []
    i = 0
    while i < len(tokens):
        t = tokens[i]
        if i + 2 < len(tokens) and t == "仓储" and tokens[i + 1] == "WMS":
            out.append(" ".join(tokens[i : i + 3]))
            i += 3
            continue
        if (
            i + 1 < len(tokens)
            and tokens[i] in ("WMS", "TMS", "MES", "OA", "CRM", "BI", "HR")
            and tokens[i + 1] in ("配置助理", "调度文员", "实施顾问助理", "流程配置专员", "报表开发外包")
        ):
            out.append(f"{tokens[i]} {tokens[i + 1]}")
            i += 2
            continue
        if i + 1 < len(tokens):
            nxt = tokens[i + 1]
            merge = (
                t in _MERGE_PREFIX
                or (t.isdigit() and len(t) <= 3)
                or (len(t) <= 5 and t.replace("&", "").isascii() and nxt and "\u4e00" <= nxt[0] <= "\u9fff")
            )
            if merge:
                out.append(f"{t} {nxt}")
                i += 2
                continue
        out.append(t)
        i += 1
    return out


def _parse_more_titles(raw: str) -> list[str]:
    titles: list[str] = []
    for line in raw.splitlines():
        line = line.strip()
        if not line:
            continue
        if " · " in line:
            titles.extend(p.strip() for p in line.split(" · ") if p.strip())
        else:
            titles.extend(_tokenize_title_line(line))
    return titles



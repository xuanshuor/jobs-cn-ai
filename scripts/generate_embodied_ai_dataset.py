def build_job(
    jid: str,
    title: str,
    emp: int,
    sal: int,
    edu: str,
    sector: str,
    ind: str,
    e: float,
    c: float,
    tier: str | None = None,
) -> dict:
    """学术级 task-capability-academic-v2；手工先验仅 12% 混合（敏感性分析可关）。"""
    res = compute_occupation_substitution(
        title=title,
        sector=sector,
        education=edu,
        employment=emp,
        salary_median=sal,
        tier=tier,
        industry_label=ind,
        prior_embodied_10=e,
        prior_cognitive_10=c,
        prior_blend=0.12,
        monte_carlo_samples=MONTE_CARLO_DEFAULT_N,
        include_task_breakdown=False,
    )
    staffing = compute_occupation_staffing_timeline(
        title=title,
        sector=sector,
        education=edu,
        employment=emp,
        salary_median=sal,
        tier=tier,
        industry_label=ind,
    )
    d: dict = {
        "id": jid,
        "title": title,
        "employment": emp,
        "salaryMedianAnnual": sal,
        "education": edu,
        "sector": sector,
        "industryLabel": ind,
        "embodiedSubstitution": res.embodied_10,
        "cognitiveAiSubstitution": res.cognitive_10,
        "hybridSubstitution": res.hybrid_10,
        "aiImpact": res.total_10,
        "substitutionShares": {
            "fullyAutomatable": res.shares.fully_automatable,
            "humanAiCollaboration": res.shares.human_ai_collaboration,
            "humanOnly": res.shares.human_only,
        },
        "substitutionTimeline": res.timeline,
        "substitutionArchetype": res.archetype,
        "aiLaborStaffing": staffing,
    }
    if res.uncertainty_p5 is not None and res.uncertainty_p95 is not None:
        d["aiImpactUncertainty"] = {
            "p5": res.uncertainty_p5,
            "p50": res.uncertainty_p50,
            "p95": res.uncertainty_p95,
        }
    if res.methodology:
        d["substitutionMethodology"] = res.methodology
    if tier:
        d["positionTier"] = tier
    return d


def _rebuild_merged_job(spec: dict) -> dict:
    job = build_job(
        spec["rep_id"],
        spec["title"],
        int(spec["employment"]),
        spec["salaryMedianAnnual"],
        spec["education"],
        spec["sector"],
        spec["industryLabel"],
        spec["embodied_prior"],
        spec["cognitive_prior"],
        spec.get("positionTier"),
    )
    job["mergedFrom"] = spec["mergedFrom"]
    job["mergeNote"] = spec["mergeNote"]
    return job


